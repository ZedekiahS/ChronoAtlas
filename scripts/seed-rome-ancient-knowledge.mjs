function stableId(value) {
  return createHash("sha256").update(String(value)).digest("hex").slice(0, 18);
}

function compactText(parts) {
  return parts
    .filter((part) => typeof part === "string" && part.trim().length > 0)
    .map((part) => part.trim())
    .join("\n\n");
}

function topicFor(card) {
  if (card.tags?.some((tag) => ["war", "army", "frontier", "campaign"].includes(tag))) return "military";
  if (card.tags?.some((tag) => ["succession", "usurpation", "dynasty", "assassination"].includes(tag))) return "succession";
  if (card.tags?.some((tag) => ["source", "historiography", "persecution"].includes(tag))) return "source_criticism";
  return "political_structure";
}

function chunkBody(documentBody) {
  return documentBody.replace(/\s+/g, " ").trim();
}

